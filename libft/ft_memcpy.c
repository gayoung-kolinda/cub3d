#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, int n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	if (!d && !s)
		return (0);
	while (n--)
		*d++ = *s++;
	return (dst);
}
